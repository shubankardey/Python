#string to integer:

string=str(input("Enter a number as string: "))
print(type(string))
string=int(string)
print("converted to string to integer:",string,type(string))

#integer to float:

integer=int(input("Enter a number as integer: "))
print(type(integer))
integer=float(integer)
print("converted to integer to float:",integer,type(integer))